def _detect_sub_intent(intent: str, query: str) -> str:
    """Pick a focused mode by keyword-matching against intent + raw query.
    Defaults to ``full_health_check`` when nothing distinctive matches.
    """
    text = f"{intent or ''} {query or ''}"
    for label, rx in _SUB_INTENT_RULES:
        if rx.search(text):
            return label
    return "full_health_check"


@dataclass
class ParsedProfile:
    user_id: str
    name: str
    age: int
    country: str
    base_currency: str
    risk_profile: str
    positions: list[dict[str, Any]]
    preferences: dict[str, Any]


def _parse_profile(user: dict) -> ParsedProfile:
    return ParsedProfile(
        user_id=str(user.get("user_id") or "unknown"),
        name=str(user.get("name") or ""),
        age=int(user.get("age") or 0),
        country=str(user.get("country") or ""),
        base_currency=str(user.get("base_currency") or "USD").upper(),
        risk_profile=str(user.get("risk_profile") or "moderate").lower(),
        positions=list(user.get("positions") or []),
        preferences=dict(user.get("preferences") or {}),
    )


def _benchmark_preference(prefs: dict[str, Any]) -> str:
    return str(prefs.get("preferred_benchmark") or "S&P 500")


def _benchmark_ticker(name: str) -> str:
    key = name.strip().upper()
    if "FTSE" in key:
        return "ISF.L"
    if "NIKKEI" in key:
        return "EWJ"
    if "MSCI" in key and "WORLD" in key:
        return "URTH"
    if key == "QQQ" or "NASDAQ-100" in key:
        return "QQQ"
    if "S&P" in name.upper() or "SP500" in key.replace(" ", "") or key == "SP500":
        return "SPY"
    return "SPY"


