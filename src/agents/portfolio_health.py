def _parse_date(iso: str) -> datetime | None:
    try:
        return datetime.fromisoformat(iso).replace(tzinfo=timezone.utc)
    except Exception:
        return None


def _close_usd_for_currency(amount_local: float, currency: str, fx: dict[str, float]) -> float:
    c = currency.upper()
    if c == "USD":
        return float(amount_local)
    if c == "EUR":
        return float(amount_local) * fx.get("EURUSD", 1.0)
    if c == "GBP":
        return float(amount_local) * fx.get("GBPUSD", 1.0)
    if c == "JPY":
        # USDJPY = yen per 1 USD → USD = JPY / USDJPY
        return float(amount_local) / fx.get("USDJPY", 150.0)
    return float(amount_local)


def _thread_last_close(symbol: str) -> float | None:
    try:
        t = yf.Ticker(symbol)
        hist = t.history(period="5d", auto_adjust=True)
        if hist is None or hist.empty:
            fi = getattr(t, "fast_info", None)
            if fi is not None and getattr(fi, "last_price", None):
                return float(fi.last_price)
            return None
        return float(hist["Close"].dropna().iloc[-1])
    except Exception as e:
        logger.debug("Price fetch failed for %s: %s", symbol, e)
        return None


async def _snapshot_fx_to_usd() -> dict[str, float]:
    async def one(sym: str) -> tuple[str, float | None]:
        val = await asyncio.to_thread(_thread_last_close, sym)
        return sym, val

    pairs = [("EURUSD=X", "EURUSD"), ("GBPUSD=X", "GBPUSD"), ("USDJPY=X", "USDJPY")]
    out: dict[str, float] = {}
    results = await asyncio.gather(*[one(sym) for sym, _ in pairs])
    defaults = {"EURUSD": 1.08, "GBPUSD": 1.27, "USDJPY": 150.0}
    for (sym, key), (_s, val) in zip(pairs, results):
        out[key] = float(val) if val is not None else defaults[key]
    return out


@retry(
    stop=stop_after_attempt(3),
    wait=wait_exponential(multiplier=0.5, min=0.5, max=4),
    retry=retry_if_exception_type(Exception),
    reraise=False,
)
async def _fetch_with_retry(tickers: list[str]) -> dict[str, float | None]:
    async def fetch_sym(sym: str) -> tuple[str, float | None]:
        px = await asyncio.to_thread(_thread_last_close, sym)
        return sym, px

    fetched = await asyncio.gather(*[fetch_sym(t) for t in tickers])
    prices = {sym: px for sym, px in fetched}
    if tickers and all(v is None for v in prices.values()):
        raise RuntimeError("all yfinance quote fetches returned None")
    return prices


async def _fetch_prices(positions: list[dict]) -> tuple[dict[str, float], list[str]]:
    """Last close per ticker in Yahoo's listing currency; warns when falling back to ``avg_cost``."""
    tickers = list({str(p.get("ticker") or "").strip() for p in positions if p.get("ticker")})
    warnings: list[str] = []
    prices_local: dict[str, float] = {}

    fetched_map: dict[str, float | None]
    try:
        fetched_map = await _fetch_with_retry(tickers)
    except Exception as e:
        logger.debug("yfinance retry wrapper failed: %s", e)
        fetched_map = {}

    for sym in tickers:
        px = fetched_map.get(sym)
        if px is None:
            # avg_cost matches position currency when quote is missing
            pcost = next((p for p in positions if str(p.get("ticker")) == sym), None)
            fb = float(pcost["avg_cost"]) if pcost and pcost.get("avg_cost") is not None else 0.0
            prices_local[sym] = fb
            warnings.append(
                f"Live quote unavailable for {sym}; using average cost ({fb:.4g}) as a placeholder price."
            )
        else:
            prices_local[sym] = px

    return prices_local, warnings


def _compute_concentration(positions_with_values: list[dict]) -> ConcentrationRisk:
    totals = [float(p["current_value"]) for p in positions_with_values]
    total = sum(totals)
    if total <= 0:
        return ConcentrationRisk(top_position_pct=0.0, top_3_positions_pct=0.0, flag="low")
    sorted_tv = sorted(totals, reverse=True)
    top1 = sorted_tv[0] / total * 100.0
    top3 = sum(sorted_tv[:3]) / total * 100.0
    if top1 < 25:
        flag = "low"
    elif top1 <= 40:
        flag = "medium"
    else:
        flag = "high"
    return ConcentrationRisk(
        top_position_pct=round(top1, 2),
        top_3_positions_pct=round(top3, 2),
        flag=flag,
    )


def _compute_performance(
    positions: list[dict],
    prices_local: dict[str, float],
    fx_to_usd: dict[str, float],
) -> tuple[Performance, datetime | None]:
    """Totals in USD; cost basis converted with spot FX (no historical FX)."""
    total_cost = 0.0
    total_current = 0.0
    oldest: datetime | None = None

    for pos in positions:
        ticker = str(pos.get("ticker") or "")
        qty = float(pos.get("quantity") or 0.0)
        avg_cost = float(pos.get("avg_cost") or 0.0)
        cur = str(pos.get("currency") or "USD").upper()
        purchased_at = pos.get("purchased_at")
        if isinstance(purchased_at, str):
            d = _parse_date(purchased_at)
            if d and (oldest is None or d < oldest):
                oldest = d

        price_local = float(prices_local.get(ticker, avg_cost))
        cost_usd = qty * _close_usd_for_currency(avg_cost, cur, fx_to_usd)
        current_usd = qty * _close_usd_for_currency(price_local, cur, fx_to_usd)
        total_cost += cost_usd
        total_current += current_usd

    if total_cost <= 0:
        return (
            Performance(
                total_return_pct=0.0,
                annualized_return_pct=0.0,
                cost_basis_total=0.0,
                current_value_total=round(total_current, 2),
            ),
            oldest,
        )

    total_return_pct = (total_current / total_cost - 1.0) * 100.0
    now = datetime.now(timezone.utc)
    years = 1.0 / 365.25
    if oldest:
        years = max((now - oldest).days / 365.25, 1.0 / 365.25)
    tr = total_current / total_cost
    annualized = (tr ** (1.0 / years) - 1.0) * 100.0

    return (
        Performance(
            total_return_pct=round(total_return_pct, 2),
            annualized_return_pct=round(annualized, 2),
            cost_basis_total=round(total_cost, 2),
            current_value_total=round(total_current, 2),
        ),
        oldest,
    )


def _period_return_pct(symbol: str, start: datetime, end: datetime) -> float | None:
    """Total return for ``symbol`` between ``start`` and ``end``.

    yfinance's batch ``download`` endpoint has been flaky upstream; ``Ticker.history``
    succeeds far more reliably for the same windows. We fall through to a coarse
    period-based query if the explicit date range comes back empty.
    """
    def _try_history(t: yf.Ticker, **kwargs) -> float | None:
        try:
            df = t.history(auto_adjust=True, **kwargs)
        except Exception as e:
            logger.debug("Benchmark history call failed for %s (%s): %s", symbol, kwargs, e)
            return None
        if df is None or df.empty:
            return None
        closes = df["Close"].dropna()
        if closes.empty:
            return None
        first = float(closes.iloc[0])
        last = float(closes.iloc[-1])
        if first <= 0:
            return None
        return (last / first - 1.0) * 100.0

    try:
        ticker = yf.Ticker(symbol)
        end_adj = end + timedelta(days=1)
        # Explicit date range first
        pct = _try_history(
            ticker,
            start=start.strftime("%Y-%m-%d"),
            end=end_adj.strftime("%Y-%m-%d"),
        )
        if pct is not None:
            return pct

        # Coarse fallback — pick the smallest yfinance period that covers the window
        days = max(1, (end - start).days)
        period = (
            "5d" if days <= 7
            else "1mo" if days <= 31
            else "3mo" if days <= 93
            else "6mo" if days <= 186
            else "1y" if days <= 380
            else "2y" if days <= 760
            else "5y"
        )
        return _try_history(ticker, period=period)
    except Exception as e:
        logger.debug("Benchmark fetch failed for %s: %s", symbol, e)
        return None


async def _compute_benchmark(
    performance: Performance,
    benchmark_name: str,
    oldest_purchase_date: datetime | None,
) -> tuple[BenchmarkComparison, bool]:
    """Bench vs portfolio over ~same window; second flag = series unavailable (placeholder return used)."""
    portfolio_return = performance.total_return_pct
    symbol = _benchmark_ticker(benchmark_name)
    end = datetime.now(timezone.utc)
    start = oldest_purchase_date or end

    bench_pct = await asyncio.to_thread(_period_return_pct, symbol, start, end)
    used_default = False
    if bench_pct is None:
        bench_pct = DEFAULT_BENCHMARK_RETURN_PCT
        used_default = True

    alpha = portfolio_return - bench_pct

    return (
        BenchmarkComparison(
            benchmark=benchmark_name,
            portfolio_return_pct=round(portfolio_return, 2),
            benchmark_return_pct=round(float(bench_pct), 2),
            alpha_pct=round(alpha, 2),
        ),
        used_default,
    )


